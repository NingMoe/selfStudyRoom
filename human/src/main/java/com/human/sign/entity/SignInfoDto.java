package com.human.sign.entity;

import java.io.Serializable;

public class SignInfoDto implements Serializable{
       
   /**
     * 
     */
    private static final long serialVersionUID = 3711344208482501685L;

    private Long activityId;
    
    private String deptorschool;//部门
    
    private Long total;//部门总人数
    
    private Long signTotal;//部门已签到人数
    
    private Long checkRate;//部门签到率

    public Long getActivityId() {
        return activityId;
    }

    public void setActivityId(Long activityId) {
        this.activityId = activityId;
    }

    public String getDeptorschool() {
        return deptorschool;
    }

    public void setDeptorschool(String deptorschool) {
        this.deptorschool = deptorschool;
    }

    public Long getTotal() {
        return total;
    }

    public void setTotal(Long total) {
        this.total = total;
    }

    public Long getSignTotal() {
        return signTotal;
    }

    public void setSignTotal(Long signTotal) {
        this.signTotal = signTotal;
    }

    public Long getCheckRate() {
        return checkRate;
    }

    public void setCheckRate(Long checkRate) {
        this.checkRate = checkRate;
    }
    
    
    
    
}
