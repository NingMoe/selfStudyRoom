package com.human.recruitment.entity;

public class PositionHighLight {
    private String positionId;
    
    private String highLight;
    
    private String highLightVal;

    public String getPositionId() {
        return positionId;
    }

    public void setPositionId(String positionId) {
        this.positionId = positionId;
    }

    public String getHighLight() {
        return highLight;
    }

    public void setHighLight(String highLight) {
        this.highLight = highLight;
    };
    
    public String getHighLightVal() {
        return highLightVal;
    }

    public void setHighLightVal(String highLightVal) {
        this.highLightVal = highLightVal;
    }

    @Override
    public String toString() {
        // TODO Auto-generated method stub
        return "{\"positionId\":\""+positionId+"\",\"highLight\":\""+highLight+"\"}";
    }
}
