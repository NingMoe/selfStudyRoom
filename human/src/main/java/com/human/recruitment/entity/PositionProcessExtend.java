package com.human.recruitment.entity;

public class PositionProcessExtend {
    private Integer positionId;
    
    private Integer positionProcessId;
    
    private String processDef;
    
    private String type;
    
    private String comid;
    
    private String dept;
    
    private String firstNodeName;
    
    private String positionName;

    public Integer getPositionId() {
        return positionId;
    }

    public void setPositionId(Integer positionId) {
        this.positionId = positionId;
    }

    public Integer getPositionProcessId() {
        return positionProcessId;
    }

    public void setPositionProcessId(Integer positionProcessId) {
        this.positionProcessId = positionProcessId;
    }

    public String getProcessDef() {
        return processDef;
    }

    public void setProcessDef(String processDef) {
        this.processDef = processDef;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getComid() {
        return comid;
    }

    public void setComid(String comid) {
        this.comid = comid;
    }

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }

    public String getFirstNodeName() {
        return firstNodeName;
    }

    public void setFirstNodeName(String firstNodeName) {
        this.firstNodeName = firstNodeName;
    }

    public String getPositionName() {
        return positionName;
    }

    public void setPositionName(String positionName) {
        this.positionName = positionName;
    }
    
    
}
