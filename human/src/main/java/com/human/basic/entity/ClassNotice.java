package com.human.basic.entity;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonProperty;

public class ClassNotice {
    @JsonProperty
    private String ReplyTimes;
    
    @JsonProperty
    private String ReplyMax;
    
    @JsonProperty
    private String MessageId;
    
    @JsonProperty
    private String RouteKey;
    
    /**
     * [{"nSchoolId":"1","sCode":"saassa"}]
     */
    @JsonProperty
    private String BodyJson;
    
    @JsonIgnore
    public String getMessageId() {
        return MessageId;
    }
    
    @JsonIgnore
    public void setMessageId(String messageId) {
        MessageId = messageId;
    }
    
    @JsonIgnore
    public String getRouteKey() {
        return RouteKey;
    }
    
    @JsonIgnore
    public void setRouteKey(String routeKey) {
        RouteKey = routeKey;
    }
    
    @JsonIgnore
    public String getReplyTimes() {
        return ReplyTimes;
    }
    
    @JsonIgnore
    public void setReplyTimes(String replyTimes) {
        ReplyTimes = replyTimes;
    }
    
    @JsonIgnore
    public String getReplyMax() {
        return ReplyMax;
    }
    
    @JsonIgnore
    public void setReplyMax(String replyMax) {
        ReplyMax = replyMax;
    }
    
    @JsonIgnore
    public String getBodyJson() {
        return BodyJson;
    }
    
    @JsonIgnore
    public void setBodyJson(String bodyJson) {
        BodyJson = bodyJson;
    }
}
