package com.human.bpm.entity;

/**
 * 任务处理人员实体
 *
 */
public class BpmTaskUser {
	public static final int USER_TYPE_AS = 1;//个人
	public static final int USER_TYPE_CU = 2;//候选人
	public static final int USER_TYPE_CG = 3;//候选组
	
	private String privNo;//人员来源:接口、流程中、数据权限配置中的、委派
	
	
	private String assigneeCode;//任务最终的处理人员工号
	 
	private String assigneeName;//任务最终的处理人员姓名
		
	private String owerCode;//未委派之前的处理人员工号，若没有委派，为空
	 
	private String owerName;//未委派之前的处理人员姓名，若没有委派，为空

	public String getPrivNo() {
		return privNo;
	}

	public void setPrivNo(String privNo) {
		this.privNo = privNo;	
	}

	public String getAssigneeCode() {
		return assigneeCode;
	}

	public void setAssigneeCode(String assigneeCode) {
		this.assigneeCode = assigneeCode;
	}

	public String getAssigneeName() {
		return assigneeName;
	}

	public void setAssigneeName(String assigneeName) {
		this.assigneeName = assigneeName;
	}

	public String getOwerCode() {
		return owerCode;
	}

	public void setOwerCode(String owerCode) {
		this.owerCode = owerCode;
	}

	public String getOwerName() {
		return owerName;
	}

	public void setOwerName(String owerName) {
		this.owerName = owerName;
	}
	
	
	
}
