package com.human.bpm.entity;

import java.util.List;

public class BpmTransition {
	private String activityName;//当前节点名称
	private boolean sel;//是否需要选择；根据是否选择分支和人员确定
	private List<BpmOutgo> outgos;
	
	public String getActivityName() {
		return activityName;
	}
	public void setActivityName(String activityName) {
		this.activityName = activityName;
	}
	public boolean isSel() {
		return sel;
	}
	public void setSel(boolean sel) {
		this.sel = sel;
	}
	public List<BpmOutgo> getOutgos() {
		return outgos;
	}
	public void setOutgos(List<BpmOutgo> outgos) {
		this.outgos = outgos;
	}
	
	
}
