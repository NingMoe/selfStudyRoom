package com.human.continuedClass.entity;

import com.human.pulgin.excel.ExcelAnnotation;
import com.human.pulgin.excel.ExportTitleAnnotation;
public class ClassToRcClassDto {
            
    @ExportTitleAnnotation()
    @ExcelAnnotation(exportName="推荐班班号")
    private String rClassCode;
    
    @ExportTitleAnnotation()
    @ExcelAnnotation(exportName="原班班号")
    private String oClassCode;
    
    @ExportTitleAnnotation()
    @ExcelAnnotation(exportName="推荐班校区")
    private String rSchoolAreaName;
    
    @ExportTitleAnnotation()
    @ExcelAnnotation(exportName="推荐班年级")
    private String rGrade;
    
    @ExportTitleAnnotation()
    @ExcelAnnotation(exportName="推荐班科目")
    private String rSubject;
    

    public String getoClassCode() {
        return oClassCode;
    }

    public void setoClassCode(String oClassCode) {
        this.oClassCode = oClassCode;
    }

    public String getrClassCode() {
        return rClassCode;
    }

    public void setrClassCode(String rClassCode) {
        this.rClassCode = rClassCode;
    }

    public String getrSchoolAreaName() {
        return rSchoolAreaName;
    }

    public void setrSchoolAreaName(String rSchoolAreaName) {
        this.rSchoolAreaName = rSchoolAreaName;
    }

    public String getrGrade() {
        return rGrade;
    }

    public void setrGrade(String rGrade) {
        this.rGrade = rGrade;
    }

    public String getrSubject() {
        return rSubject;
    }

    public void setrSubject(String rSubject) {
        this.rSubject = rSubject;
    }
        
    
}
